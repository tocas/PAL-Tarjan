package pal;

public class Node {
	private Node parent;
	private int id;
	
	
	public Node(int i) {
		id = i;
	}
	public Node getParent() {
		return parent;
	}
	public void setParent(Node parent) {
		this.parent = parent;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
}
